# the Observer Pattern. 
# This is is a behavioural design pattern that defines 
# a one-to-many dependency relationship between objects, 
# so that when one object (the subject or observable) changes state, 
# all its dependents (observers) are notified and updated automatically. 
# This pattern is used to establish communication between objects in a 
# loosely coupled manner, where the subject and observers remain 
# independent of each other.

